--CREATE USER SPECIFICALLY FOR USE IN OUR PYTHON APPLICATION
CREATE USER
  "ApplicationUser"
WITH
  PASSWORD 'CoronaSux2020!';

--GRANT USER ACCESS TO CERTAIN CAPABILITIES
GRANT
  SELECT,
  INSERT,
  UPDATE,
  DELETE
ON
  ALL TABLES
IN
  SCHEMA public
TO
  "ApplicationUser";

GRANT
  ALL PRIVILEGES
ON
  ALL SEQUENCES
IN
  SCHEMA public
TO
  "ApplicationUser";

ALTER
  DEFAULT PRIVILEGES
IN
  SCHEMA public
GRANT
  SELECT,
  INSERT,
  UPDATE,
  DELETE
ON
  TABLES
TO
  "ApplicationUser";
